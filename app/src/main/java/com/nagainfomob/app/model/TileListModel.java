public class TileListModel {

    private String tile_name;
    private String tile_dimen;
    private String tile_brand;
    private String tile_count;
    private String tile_rate;
    private String tile_price;
    private String type;

    public String getTile_name() {
        return tile_name;
    }
    public void setTile_name(String tile_name) {
        this.tile_name = tile_name;
    }

    public String getTile_dimen() {
        return tile_dimen;
    }
    public void setTile_dimen(String tile_dimen) {
        this.tile_dimen = tile_dimen;
    }

    public String getTile_brand() {
        return tile_brand;
    }
    public void setTile_brand(String tile_brand) {
        this.tile_brand = tile_brand;
    }

    public String getTile_price() {
        return tile_price;
    }
    public void setTile_price(String tile_price) {
        this.tile_price = tile_price;
    }

    public String getTile_count() {
        return tile_count;
    }
    public void setTile_count(String tile_count) {
        this.tile_count = tile_count;
    }

    public String getTile_rate() {
        return tile_rate;
    }
    public void setTile_rate(String tile_rate) {
        this.tile_rate = tile_rate;
    }

    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }

}
